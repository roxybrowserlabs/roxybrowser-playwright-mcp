import { mkdir, readFile, writeFile } from "node:fs/promises";
import os from "node:os";
import path from "node:path";
import { fileURLToPath } from "node:url";

import { build } from "vite";

const repoRoot = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const vendorRoot = path.join(repoRoot, "src/vendor/playwright");
const sourceRoot = path.join(vendorRoot, "source");
const entry = path.join(sourceRoot, "injected/injectedScript.snapshot.ts");
const outDir = path.join(os.tmpdir(), "roxybrowser-playwright-snapshot-source");
const bundleName = "injectedScriptSource.js";
const generatedFile = path.join(vendorRoot, "generated/injectedScriptSource.ts");

await build({
  configFile: false,
  root: repoRoot,
  logLevel: "silent",
  build: {
    emptyOutDir: true,
    minify: false,
    outDir,
    sourcemap: false,
    target: "es2022",
    lib: {
      entry,
      formats: ["iife"],
      name: "__roxyPlaywrightSnapshotSource"
    },
    rollupOptions: {
      output: {
        entryFileNames: bundleName
      }
    }
  }
});

const bundledSource = await readFile(path.join(outDir, bundleName), "utf8");
const source = `/**
 * Generated by scripts/generate-playwright-snapshot-source.mjs.
 *
 * Source entry: src/vendor/playwright/source/injected/injectedScript.snapshot.ts
 * Do not edit this file directly; edit the readable source files instead and
 * run pnpm build:vendor:snapshot.
 */

export const PLAYWRIGHT_INJECTED_SCRIPT_SOURCE = ${JSON.stringify(bundledSource)};
`;

await mkdir(path.dirname(generatedFile), { recursive: true });
await writeFile(generatedFile, source);
