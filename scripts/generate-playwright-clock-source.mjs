import { mkdir, mkdtemp, readFile, rm, writeFile } from "node:fs/promises";
import os from "node:os";
import path from "node:path";
import { fileURLToPath } from "node:url";

import { build } from "vite";

const repoRoot = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const playwrightClockSource = path.join(
  repoRoot,
  "library/playwright/packages/injected/src/clock.ts"
);
const vendorRoot = path.join(repoRoot, "src/vendor/playwright");
const generatedFile = path.join(vendorRoot, "generated/clockSource.ts");
const bundleName = "clockSource.js";
const tempDir = await mkdtemp(path.join(os.tmpdir(), "roxybrowser-playwright-clock-source-"));
const entryFile = path.join(tempDir, "clock.entry.ts");

await writeFile(
  entryFile,
  [
    `import { inject } from ${JSON.stringify(playwrightClockSource)};`,
    "",
    "(globalThis).__roxyPlaywrightClockBundle = { inject };",
    "",
    "export { inject };",
    ""
  ].join("\n")
);

try {
  await build({
    configFile: false,
    root: repoRoot,
    logLevel: "silent",
    build: {
      emptyOutDir: true,
      minify: false,
      outDir: tempDir,
      sourcemap: false,
      target: "es2022",
      lib: {
        entry: entryFile,
        formats: ["iife"],
        name: "__roxyPlaywrightClockSource"
      },
      rollupOptions: {
        output: {
          entryFileNames: bundleName
        }
      }
    }
  });

  const bundledSource = await readFile(path.join(tempDir, bundleName), "utf8");
  const source = `/**
 * Generated by scripts/generate-playwright-clock-source.mjs.
 *
 * Source entry: library/playwright/packages/injected/src/clock.ts
 * Do not edit this file directly; edit the upstream readable source and
 * re-run this generator.
 */

export const PLAYWRIGHT_CLOCK_SOURCE = ${JSON.stringify(bundledSource)};
`;

  await mkdir(path.dirname(generatedFile), { recursive: true });
  await writeFile(generatedFile, source);
} finally {
  await rm(tempDir, { force: true, recursive: true });
}
